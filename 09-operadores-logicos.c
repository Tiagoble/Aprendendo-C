#include<stdio.h>

void main(){
    int a=1, b=2;

    printf("a = %d, b = %d\n",a,b);

    //menor que
    printf("a < b : %d\n", a < b);

    //maior que
    printf("a > b : %d\n", a > b);

    //equivalente
    printf("a == b : %d\n", a == b);

    //menor ou igual
    printf("a <= b : %d\n", a <= b);

    //maior ou igual
    printf("a >= b : %d\n", a >= b);

    //diferente
    printf("a != b : %d\n", a != b);
}